public class MathFunctions {
    public static void main(String args[]){
        System.out.println("Maximum of numbers: "+ Math.max(6, 5));
        System.out.println("Minimum of numbers: "+ Math.min(6, 5));
        System.out.println("Squareroot of number: "+ Math.sqrt(8));
        System.out.println("Power: "+ Math.pow(2, 3));
        System.out.println("Absolute of number: "+ Math.abs(-8));//gives positive of value entered
    }
}
